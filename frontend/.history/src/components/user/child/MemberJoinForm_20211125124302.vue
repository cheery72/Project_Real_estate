<script>
import { joinMember, checkId } from "@/api/member";
export default {
  name: "MemberJoinForm",
  data() {
    return {
      user: {
        userid: "",
        userpwd: "",
        userpwd_ck: "",
        username: "",
        email: "",
      },
      idresult: "",
      validcheck: "", //비밀번호 유효성
      pwcheck: "", //비밀번호 체크
    };
  },
  props: {},
  created() {},
  methods: {
    //비밀번호 validation체크
    validPw() {
      var reg =
        /^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$%^&*-]).{8,}$/;
      var pw = this.user.userpwd;

      if (false === reg.test(pw)) {
        this.validcheck =
          "비밀번호는 8자 이상이어야 하며, 숫자/대문자/소문자/특수문자를 모두 포함해야 합니다.";
      } else {
        console.log("통과");
        this.validcheck = "유효합니다.";
      }
    },
    //비밀번호 체크
    checkPw() {
      if (this.user.userpwd != this.user.userpwd_ck) {
        console.log("체크실패");
        this.pwcheck = "위 비밀번호와 다릅니다.";
      } else {
        console.log("체크성공");
        this.pwcheck = "위 비밀번호와 동일합니다.";
      }
    },
    onSubmit(event) {
      event.preventDefault();
      this.registerMember();
    },
    onReset(event) {
      event.preventDefault();
      this.user.userid = "";
      this.user.userpwd = "";
      this.user.username = "";
      this.user.email = "";
    },
    idCheck() {
      checkId(
        this.user.userid,
        ({ data }) => {
          let cnt = data.idcount;
          if (cnt == 0) {
            this.idresult = this.user.userid + "는 사용 가능합니다.";
          } else {
            this.idresult = this.user.userid + "는 사용할 수 없습니다.";
          }
        },
        (error) => {
          console.log(error);
        }
      );
    },
    registerMember() {
      joinMember(
        this.user,
        ({ data }) => {
          let msg = "회원가입 처리시 문제가 발생했습니다.";
          if (data === "success") {
            msg = "회원가입이 완료되었습니다.";
          }
          alert(msg);
          this.$router.push({ name: "SignIn" });
        },
        (error) => {
          console.log(error);
        }
      );
    },
  },
};
</script>

